public class DesafioArrays2 {

    public static void main(String[] args) {

        String[][] multicores = {
                {"vermelho", "preto", "branco"},
                {"roxo", "azul"}
        };

        System.out.println( multicores[0][0] );  // vermelho
        System.out.println( multicores[1][0] ); // roxo
        System.out.println( multicores.length ); // 2
        System.out.println( multicores[0].length ); // 3

    }

}
